package com.fitness.ActivityService.model;

/**
 * Enumeration of the different types of activities that can be tracked.
 */
public enum ActivityType {
    /**
     * Represents a walking activity.
     */
    WALKING,

    /**
     * Represents a running activity.
     */
    RUNNING,

    /**
     * Represents a cycling activity.
     */
    CYCLING,

    /**
     * Represents a weight training activity.
     */
    WEIGHT_TRAINING,

    /**
     * Represents a yoga activity.
     */
    YOGA,

    /**
     * Represents a general cardio activity.
     */
    CARDIO
}
