package com.fitness.AiService.model;

/**
 * Enum representing the different types of activities that can be tracked.
 * This enum is used to categorize activities and can be extended with additional
 * activity types as needed.
 */
public enum ActivityType {
    /**
     * Represents a running activity.
     */
    RUNNING,

    /**
     * Represents a cycling activity.
     */
    CYCLING,

    /**
     * Represents a swimming activity.
     */
    SWIMMING,

    /**
     * Represents a weightlifting activity.
     */
    WEIGHTLIFTING,

    /**
     * Represents a yoga session.
     */
    YOGA,

    /**
     * Represents a walking activity.
     */
    WALKING,

    /**
     * Represents a hiking activity.
     */
    HIKING,

    /**
     * Represents a dancing activity.
     */
    DANCING
}
